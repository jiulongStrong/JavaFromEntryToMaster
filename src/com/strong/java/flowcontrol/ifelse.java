public class ifelse {
    public static void main(String[] args) {
        int a = 10;
        if (a > 0) {
            System.out.println("a是正数");
        } else if (a < 0) {
            System.out.println("a是负数");
        } else {
            System.out.println("a是零");
        }
    }
}
